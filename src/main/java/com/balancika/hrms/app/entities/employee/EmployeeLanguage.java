package com.balancika.hrms.app.entities.employee;

public class EmployeeLanguage {
	
	private String Emp_ID;
	private String Language;
	private String Language_ReadLevel;
	private String Language_WriteLevel;
	private String Language_SpeakLevel;
	private String Language_UnderstandLevel;
	private String Language_Type;
	
	public String getEmp_ID() {
		return Emp_ID;
	}
	public void setEmp_ID(String emp_ID) {
		Emp_ID = emp_ID;
	}
	public String getLanguage() {
		return Language;
	}
	public void setLanguage(String language) {
		Language = language;
	}
	public String getLanguage_ReadLevel() {
		return Language_ReadLevel;
	}
	public void setLanguage_ReadLevel(String language_ReadLevel) {
		Language_ReadLevel = language_ReadLevel;
	}
	public String getLanguage_WriteLevel() {
		return Language_WriteLevel;
	}
	public void setLanguage_WriteLevel(String language_WriteLevel) {
		Language_WriteLevel = language_WriteLevel;
	}
	public String getLanguage_SpeakLevel() {
		return Language_SpeakLevel;
	}
	public void setLanguage_SpeakLevel(String language_SpeakLevel) {
		Language_SpeakLevel = language_SpeakLevel;
	}
	public String getLanguage_UnderstandLevel() {
		return Language_UnderstandLevel;
	}
	public void setLanguage_UnderstandLevel(String language_UnderstandLevel) {
		Language_UnderstandLevel = language_UnderstandLevel;
	}
	public String getLanguage_Type() {
		return Language_Type;
	}
	public void setLanguage_Type(String language_Type) {
		Language_Type = language_Type;
	}
	
	
}
